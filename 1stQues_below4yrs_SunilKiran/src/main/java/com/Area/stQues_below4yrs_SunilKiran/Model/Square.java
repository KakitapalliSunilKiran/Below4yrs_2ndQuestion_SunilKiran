package com.Area.stQues_below4yrs_SunilKiran.Model;

public class Square {
	private String type;
	private double side;
	private double area;
	public double getArea() {
		return area;
	}
	public void setArea(double area) {
		this.area = area;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public double getSide() {
		return side;
	}
	public void setSide(double side) {
		this.side = side;
	}
	
}
